package Inheritence;

public class Demo 
{
   public Demo() //done
   {
	   System.out.println("super class constructor without parameter get called");
   }
   public Demo(int a)
   {
	   System.out.println("super class constructor with parameter get called"+a);
   }
   public Demo(int a, int b)
   {
	   System.out.println(" super class constructor with parameter"+a +" "+b );
   }
}
